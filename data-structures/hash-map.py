"""Below is a basic implementation of hash-map in python - Hash-map stores key value pairs"""

months = {
    1: 'January',
    2: 'February',
    3: 'March',
    4: 'April',
    5: 'May',
    6: 'June',
    7: 'July',
    8: 'August',
    9: 'September',
    10: 'October',
    11: 'November',
    12: 'December'
}

for month_num, month_name in months.items():
    print(month_num, month_name)